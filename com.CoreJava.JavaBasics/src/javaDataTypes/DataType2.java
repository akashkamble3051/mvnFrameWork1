package javaDataTypes;

public class DataType2 {

	public static void main(String[] args) {
//		Syntax to declare and initialize a variable

//		data_type variable_name = value;

		int i = 1; // declaration and initialization
		boolean bl = false;
		float f = 45.84f;
		double d = 85.26;
		char ch = 'a';

//		declaring more than one variable in one line (* all variable should be of same data type)
		long p, q, r, s; // we have declared 4 variable
		char ch1, ch2, ch3, ch4, ch5, ch6; // one can declare any number of variable

//		declaring and initializing multiple variables in one line (* all variable should be of same data type)
		int a = 5, a1 = 8, a2 = 14, a3 = 64;

//		declaring and initializing few variable
		double d1, d2 = 154.15, d3, d4, d5 = 164.54;
	}

}
