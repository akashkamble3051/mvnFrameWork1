package javaLoops;

public class ForLoop {

	public static void main(String[] args) {
//		i++ --> increment the value of i by 1, i++ ==> i = i + 1

		for (int i = 1; i <= 20; i++) {
			System.out.println(i);
		}

	}

}
// I want to print first 20 natural numbers

// Step1: initialize i with 1
// Step2: check the condition
//		--> if true then execute for loop body
//		--> if false then don't execute for loop body and execution will come out of for loop
// Step3: if for loop body is executed, after that operation part will be executed
// Step4: goto step2
