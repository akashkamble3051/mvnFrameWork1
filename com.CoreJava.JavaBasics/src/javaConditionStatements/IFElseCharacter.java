package javaConditionStatements;

// Java stores the characters based on their ASCII value
// And we can compare characters based on their ASCII value

public class IFElseCharacter {

	public static void main(String[] args) {
		System.out.println((int) 'A');
		char ch = 'S';
		if (ch >= 'A') {
			System.out.println((int) ch); // it will print ASCII value
		} else {
			System.out.println("********");
		}

	}

}
